public class MovieInformation {
    String title;
    String nameAndSurnameDirector;
    int movieTimeInMinutes;
    int movieYear;

    public MovieInformation(String title, String nameAndSurnameDirector, int movieTimeInMinutes, int movieYear) {
        this.title = title;
        this.nameAndSurnameDirector = nameAndSurnameDirector;
        this.movieTimeInMinutes = movieTimeInMinutes;
        this.movieYear = movieYear;
    }
}
